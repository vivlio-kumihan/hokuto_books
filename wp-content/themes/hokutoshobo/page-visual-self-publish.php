        <h4 class="main__header4">写真集／画集の仕様について</h4>
        <p>写真集の原稿づくりは、前もって本の大きさや全体のページ数を含めた仕様をお決めになられることをお勧めします。先に全体の完成イメージを定めることで、この後の原稿作成が進めやすくなります。</p>

        <h5 class="main__header5">ページ数</h5>
        <p>撮り溜められた写真の点数は膨大な数と思われますので、仕様を決めずに写真の選別を進めると「あれも載せたい、これも載せたい」となり、ページ数が増えてしまいがちです。</p>
        <p>最初に「何ページの写真数にする、掲載写真は何点にする」と、全体の大枠を決めておくすることで、数多ある作品のなかから掲載写真を絞り込みやすくなります。</p>

        <h5 class="main__header5">大きさ</h5>
        <p>画集や写真集をお考えの場合は、本の大きさにひと工夫が必要です。</p>
        <p>一般的な画集や写真集は、B5判やA4判で作られることが多いのですが、気軽に手に取ることができる本を目指すのでしたら、A5判やB6判など、文字主体の冊子でよく用いられる大きさで仕上げるのもひとつの方法です。</p>
        <p>北斗書房では、「正方形の本」をお勧めしています。縦長・横長どちらの絵や写真であっても、収まり良く仕上げることができるからです。</p>

        <h5 class="main__header5">用紙</h5>
        <p>写真集や画集の場合は、コート紙・マットコート紙が適しています。どちらの紙も、表面に白色顔料を塗って滑らかに加工した紙です。</p>
        <p>表面に光沢があるものをコート紙、表面にツヤ消し加工を施したものをマットコート紙と呼びます。白色顔料を塗るのは、インクの発色を良くするためで、写真集や画集の本文用紙によく用いられます。</p>

        <h4 class="main__header4">色校正について</h4>
        <p>制作の流れでは、一般的な文字校正とは別に「色校正」を行います。色校正は、実際に試し刷りを行う本機校正と、専用のプリンターで行う簡易色校正（プルーフ）の、２通りがあります。</p>
        <p>再現の正確さやコストで特徴がありますので、どちらの方法で色校正を行うのかは、事前に打ち合わせを行います。</p>

        <h4 class="main__header4">カラー印刷の発色について</h4>
        <p>カラー印刷は、インクの三原色である紅（シアン）・青（マゼンタ）・黄（イエロー）と、黒の４色で印刷します。一方、パソコンやカメラのモニターは光の三原色であるR（レッド）・G（グリーン）・B（ブルー）を用いて表現します。</p>
        <p>例えば写真集を制作する場合ですと、次のような流れになります。</p>
        <ul class="main__list remove-mark">
          <li>１．作品をデジタルカメラで撮影（RGB）</li>
          <li>２．パソコンでデータをチェック（RGB）</li>
          <li>３．印刷会社で制作（RGB→CMYKに変換）</li>
          <li>４．印刷（CMYK）</li>
        </ul>
        <p>印刷用は色の三原色＋黒（CMYK）を用いますので、光の三原色であるRGBのままでは印刷できません。そのため、３の工程でRGBからCMYKに変換します。デジタルカメラやパソコンのモニタで見た色調と、印刷機で印刷した色調が異なる理由はここにあります。</p>
