        <section>
          <h4 class="main__header4">エッセイ</h4>
          <div class="image-wrapper ami about-essay">
            <img src="<?php echo get_template_directory_uri(); ?>/images/article/page-novel-self-publish-02.jpg" />
          </div>
          <p>エッセイとは「日常の出来事や世間の事象に自分の意見や感想を交え、自由な形式で書いたもの」です。これらを、１冊の本にまとめたものは「エッセイ集」と呼びます。</p>
          <p>過去のエピソードをエッセイ風にまとめることで、自分史を作ることもできます。この手法は、芸能人の自伝などでもよく見られます。</p>

          <h5 class="main__header5">原稿の書き方</h5>
          <p>「長文はちょっと荷が重いけど、短い文章なら・・・」という方にエッセイはぴったりです。</p>
          <p>エッセイの場合は「まず原稿を書いてみる」ことをお勧めしています。本全体の構成や前後関係などもあまり考えずに、書きたいことから書き進めます。文章量は、１本のエッセイで1,000～2,000字程度（A5判の本で2～4ページ位）もあれば充分です。何本か書いたら、並び順やグループ分けによって構成を考え1冊の本にまとめます。これは「考えてから書く」のではなく「書いてから考える」方法になります。</p>

          <h5 class="main__header5">エッセイ集の構成</h5>
          <p>エッセイの構成は、インターネットの世界に例えると「ブログ」に近いといえます。ブログは、日々書き記した内容に「タグ」を付けることで、いくつかのグループにまとめることができます。</p>
          <p>エッセイの場合も同様で、ある程度文章が書き溜められたらグループ分けを行います。時系列でまとめるのが一般的ですが、テーマ別でまとめても良いでしょう。まとめ方次第で、他に例のない面白い作品に仕上げることもできます。</p>

          <h5 class="main__header5">題材に困ったときは</h5>
          <p>何を書いていいか考え付かないときは「キーワード連想法」をお勧めします。これは、企画の会議などで行われる「ブレーンストーミング」と「KJ法」を応用したものです。エッセイ風自分史の場合ですと、進め方は次のようになります。</p>

          <ul class="description">
            <li>
              <div class="description__title">
                1. キーワードの書き出し
              </div>
              <div class="description__content">
                関連するキーワード（例：「学園祭」「初恋」「転勤」「初仕事」「幼馴染」など）を、思いつく限りできるだけたくさんカードに書き出します。
              </div>
            </li>
            <li>
              <div class="description__title">
                2. 組み合わせ連想
              </div>
              <div class="description__content">
                書いたカードを並べ変えて組み合わせながら色々と連想することで、エッセイの題材にできる具体的な内容を思い出していきます。
              </div>
            </li>
          </ul>
          <p>ひとつの出来事を思い出すと、そこから関連して色々と思い出されてくるものです。 忘れていたことを、あれこれ連想するなかで思い出す作業は、意外と楽しいものです。 はじめから文章にしようと思わずに、ゲーム感覚で取り組まれると良いでしょう。</p>

          <h5 class="main__header5">ワンポイントアドバイス</h5>
          <p>この方法の注意点は、原稿を書き上げた後の見直しをしっかりすることです。「とりあえず書いてみる」という書き方で進めると、年月日や場所などを曖昧なままにしてしまうことがよく起きます。
          <p>原稿を書き上げた後で、日記やアルバム等の資料と突き合わせて、内容の正確さを高めることをお勧めします。手元に資料もなく、はっきりした時期が思い出せない場合は、当時の世相や出来事と対比させると、時期の特定がしやすくなります。</p>
          </p>

