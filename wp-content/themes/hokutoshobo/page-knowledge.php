          <h4 id="about-caliper" class="main__header4">紙の厚さ</h4>
          <p>紙の厚さは重さで表示されます。主に「連量」という単位が用いられます。<br />
            連量は、原紙（Ａ全判や四六判など）1,000枚枚あたりの重さであらわします。<br />
            たとえば、一般的に流通しているコピー用紙は、連量に換算すると「A判35㎏」「四六判55㎏」となります。<br />
            連量表記の場合は、同じ厚さでも紙の大きさによって重さが異なることから「○判○㎏」のように、原紙の大きさとセットで表示されます。<br />
            本の表紙に使う、あるいは本文用紙として用いる、同じ紙でもその使い道によって、適切な紙の厚さは異なります。</p>
