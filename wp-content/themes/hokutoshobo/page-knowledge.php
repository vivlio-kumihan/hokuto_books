      <h3 class="main__header3">著作権について</h3>
      <p>近年、コンプライアンス（法令遵守）に対する意識が高まっています。<br />
        著作権侵害に関するニュースを目にする機会も増えています。<br />
        これは、著作物に関する権利や法律の意識が高くなっているといえます。<br />
        もちろん、自費出版も例外ではありません。</p>
      <h5 class="main__header5">著作権とは</h5>
      <p>著作権はコピーライトとも呼ばれる「知的財産権」のひとつで、著作物を創作した際に著者に発生する権利です。<br />
        著作権法では、著作物の複製、貸与、放送などの権利は、著者が独占的に持つとしています。<br />
        言い換えると「著作物は著者のもの」ということになります。</p>
      <h5 class="main__header5">著作権の侵害</h5>
      <p>著者の許可なく勝手に複製したり、発表したりすることは著作権の侵害になります。<br />
        自費出版も例外ではなく、作品の著作権は著者にありますが、その一方で他人の著作物を無断で転載することは他の著者の著作権を侵害することになります。</p>
      <h5 class="main__header5">引用</h5>
      <p>一定の条件を満たした場合は、自分の著作に他人の著作の一部を引用することは許されます。<br />
        著作権法第32条では、引用の定義を次のように定められています。</p>
      <ol class="main__order-list">
        <li>引用することが、オリジナルの文章（本文）を表現するために必要であること</li>
        <li>主量的にも質的にも自分のオリジナルな文章が主であり、引用部分が従であること</li>
        <li>引用部分とその他の部分が明確に区別し引用文は改変しないこと</li>
        <li>引用された著作物の出典が明示されていること</li>
        <li>この他、歌詞や写真など、著作物の種類によって気を付けるべき点は異なります。
      </ol>
