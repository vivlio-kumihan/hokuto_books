          </div>
          <p>ISBNコードは正式には「国際標準図書番号」と呼ばれます。<br />
            世界中で発行される書籍の、発行国、出版社名、タイトルを記録するための「本の戸籍」のようなものです。<br />
            日本では1981年からこの国際団体に加盟し、出版物へのISBNの記載を推進しています。<br />
            ISBNコードを取得した書籍は東京の国会図書館にも保存され、日本国内で発行された正式な書籍として登録されます。</p>
          <p>ISBNコードは、本来はすべての出版物に対して付与することが望ましい、世界共通の国際標準図書番号です。書籍を書店に流通するためだけに必要なものではありません。書店流通をしない自費出版や、ごく親しい方に配る30冊だけの私家版であってもISBNを取得できます。<br />
            弊社は、自費出版の会社としてISBN出版者記号を取得しており、自社で制作した自費出版作品には、無償でこの「ISBNコード」をお付けしております。ISBNコードを付けることによって、お客様の自費出版作品は正式に日本で発行された書籍として登録されます。</p>
          <p>ISBNコードは、書籍の奥付と表紙カバーなどに記載させていただき、弊社より国立国会図書館に納本をいたします。</p>
