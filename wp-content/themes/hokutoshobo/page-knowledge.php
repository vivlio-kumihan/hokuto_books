      <h3 class="main__header3">仕様について</h3>
      <h4 class="main__header4">用紙</h4>
      <p class="margin-top-dot75em">紙の選択は、本の仕上りに大きく影響します。</p>
      <p>小説やエッセイのような文字が主体の本と、画集や写真集に代表される図版が主体の本では、適切な紙は異なります。</p>
      <p>イメージ通りの本に仕上げるためにも、紙選びは大変重要です。</p>
      <h5 class="main__header5">上質紙</h5>
      <p>コピー用紙やノートなどにもよく用いられます。<br />
        光沢が無いので文字が読みやすいことから、学術機関の論文集や、行政機関の報告書などによく用いられます。<br />
        表面は少しざらついた質感で、筆記用具での書き込みにも適しています。<br />
        反面、カラー印刷に用いると、色がくすんで表現されますので、絵や写真を綺麗に見せようとする場合には不適切な紙となります。</p>
      <h5 class="main__header5">コート紙・マットコート紙</h5>
      <p>どちらも、上質紙の表面に白色顔料を塗って滑らかに加工した紙です。<br />
        白色顔料を塗るのは、インクの発色が良くするためで、写真集や画集などの美術書の本文用紙の他、カラー印刷のチラシに適しています。<br />
        表面に光沢があるものをコート紙、顔料を塗布した後にツヤ消し加工を施したものをマットコート紙と呼びますが、製造工程はほぼ同じなので、特性もよく似ています。<br />
        上質紙と比べて紙のコストは上がりますが、その発色の良さからカラー印刷に非常に適しています。<br />
        一般的にツヤが有って見た目が華やかなコート紙はチラシやパンフレットに、余白がツヤ消しで落ち着いた感じのマットコート紙は写真集や画集に用いられることが多いようです。</p>
      <h5 class="main__header5">書籍用紙</h5>
      <p>書籍用紙は冊子の本文専用につくられた紙です。紙質は上質紙に似ていますが、紙の色は淡いクリーム色で、これは紙の製造工程で染色していることによります。<br />
        小説や自分史のような本の場合は、長時間文章を読むことになります。このため、紙の白色度を下げた淡いクリーム色の紙を用いることで、目を疲れにくくします。<br />
        また、腰が柔らかくページがめくりやすいことも特徴です。<br />
        上質紙と比較してややコストは上がりますが、読み物の本文紙としては最適で、北斗書房ではほとんどの場合この用紙をお勧めしています。</p>
      <p>この他にも、特殊な紙はたくさんあります。<br />
        店頭にサンプルを取り揃えていますので、本の装丁でお困りの方はぜひご相談ください。</p>
      <div class="to-page-top"><a href="#contents">目次にもどる</a></div>
      <h4 class="main__header4">紙の厚さ</h4>
      <p>紙の厚さは重さで表示されます。主に「連量」という単位が用いられます。<br />
        連量は、原紙（Ａ全判や四六判など）1,000枚枚あたりの重さであらわします。<br />
        たとえば、一般的に流通しているコピー用紙は、連量に換算すると「A判35㎏」「四六判55㎏」となります。<br />
        連量表記の場合は、同じ厚さでも紙の大きさによって重さが異なることから「○判○㎏」のように、原紙の大きさとセットで表示されます。<br />
        本の表紙に使う、あるいは本文用紙として用いる、同じ紙でもその使い道によって、適切な紙の厚さは異なります。</p>
      <div class="to-page-top"><a href="#contents">目次にもどる</a></div>
      <h4 class="main__header4">本の大きさ</h4>
      <p>本の大きさは、制作する本の内容によってある程度決まります。<br />
        画集や写真集のように、図版を大きく見せたい場合は、B4判やA4判が良いでしょう。<br />
        自分史や小説、句歌集のような、手に取ってじっくり読ませたい本は、やや小ぶりのA5判やB6判をお勧めします。<br />
        代表的な本の大きさと、適している本の種類は次のとおりです。</p>
