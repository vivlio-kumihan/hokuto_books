            <h4>本文紙の種類には何がありますか？</h4>
          </dt>
          <dd> <span>
              <p>通常モノクロ印刷の本文には、上質紙または書籍用紙が用いられます。</p>
              <p>上質紙は白色で図や文字がきれいに見れます。</p>
              <p>一方、書籍用紙は薄いクリーム色の紙で、これは光の反射を押さえ読んでいて目が疲れにくくなります。</p>
              <p>一般的には、文字書体の読み物では、書籍用紙を用いる場合が多いです。</p>
              <p>また、画集や写真集など、カラーの発色が求められる場合には、コート紙やマットコート紙が用いられます。インクの発色が良く、鮮やかなカラー印刷が可能になります。</p>
            </span></dd>
