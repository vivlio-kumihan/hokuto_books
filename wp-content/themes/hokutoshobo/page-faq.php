            <h4>奥付とは何ですか？</h4>
          </dt>
          <dd> <span>
              <p>冊子の最終ページには、「本のタイトル」「著者名」「発行所」「発行日」などを記載します。これを「奥付」と呼びます。例えるなら本の履歴書のようなものです。</p>
              <p>自費出版の場合、奥付の書式には特に決まりはありません。</p>
              <p>以前は、ほとんどの場合著者様のご住所やお電話番号が入っていましたが、最近は個人情報保護やその他トラブル回避の観点から、著者様のご連絡先を省略される場合も増えてきました。</p>
            </span></dd>
          <dt>
            <h4>PP加工とは何ですか？</h4>
          </dt>
          <dd> <span>
              <p>印刷した表紙やカバーの表面にフィルムを圧着させる加工のことで、見た目が美しくなるだけではなく、汚れやキズを防ぐ効果もあります。</p>
              <p>カバーや、カバーを付けない場合の本体表紙に使用します。</p>
              <p>PP加工は、光沢ありと光沢なしが選べます。</p>
            </span></dd>
